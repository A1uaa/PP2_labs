#Text Type:
x = str("Hello World")	#str

#Numeric Types:	
x = int(20)	#int	
x = float(20.5)	#float	
x = complex(1j)	#complex	

#Sequence Types:
x = list(("apple", "oppo", "redmi"))	#list	
x = tuple(("apple", "oppo", "redmi"))	#tuple	
x = range(6)#	range
	
#Mapping Type:
x = dict(name="Aiym", age=17)	#dict	

#Set Types:
x = set(("apple", "oppo", "redmi"))	#set	
x = frozenset(("apple", "oppo", "redmi"))	#frozenset	

#Boolean Type:
x = bool(5)#	bool

#Binary Types:	
x = bytes(5)	#bytes	
x = bytearray(5)	#bytearray	

